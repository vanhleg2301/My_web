    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("register.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        String email = request.getParameter("Email");
        String password = request.getParameter("Password");

        String rcustomerID = request.getParameter("CustomerID");
        String rgender = request.getParameter("gender");

        int customerID = Integer.parseInt(rcustomerID);
        boolean gender = Boolean.parseBoolean(rgender);
        DAO dao = new DAO();

// Check if the email exists in the database
        if (dao.existed(email)) {
            String ms = "Email " + email + " already exists";
            request.setAttribute("ms", ms);
            request.getRequestDispatcher("register.jsp").forward(request, response);
        } else {
            // Assuming check() method returns null if no account found
            Account a = dao.check(email, password);

            if (a != null) {
                String ms = "An account with the provided email and password already exists.";
                request.setAttribute("ms", ms);
                request.getRequestDispatcher("register.jsp").forward(request, response);
            } else {
                Account regis = new Account(email, password, customerID, gender, 2);
                dao.register(regis);
                HttpSession session = request.getSession();
                session.setAttribute("account", regis);
                request.getRequestDispatcher("login.jsp").forward(request, response);
            }
        }
    }

